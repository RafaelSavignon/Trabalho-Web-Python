numero1 = int(input("Digite o primeiro numero: "))
numero2 = int(input("Digite o segundo numero: "))

soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

print(f"a soma de {numero1} e {numero2} é igual a {soma}")
print(f"a subtração de {numero1} e {numero2} é igual a {subtracao}")
print(f"a multiplicação de {numero1} e {numero2} é igual a {multiplicacao}")
print(f"a divisão de {numero1} e {numero2} é igual a {divisao}")